    public void llenarTabla(ArrayList<Huesped> listado){
        String []Datos = new String [3];
        for (Huesped h : listado) {
            Datos[0] = h.getNombre();
            Datos[1] = h.getDocumento();
            Datos[2] = h.getTelefono();
            modelo.addRow(Datos);
        }
    }
    public MostrarHuespedes() {
        initComponents();
        setLocationRelativeTo(null);
        modelo = new DefaultTableModel();
        modelo.addColumn("NOMBRE");
        modelo.addColumn("DOCUMENTO");
        modelo.addColumn("TELEFONO");
        this.tablaHuesped.setModel(modelo);
        ControladorHuesped chu = new ControladorHuesped();
        llenarTabla(chu.Leer());
    }
